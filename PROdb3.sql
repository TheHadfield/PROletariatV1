create table party(
party_name varchar (6) primary key,
government varchar (1) NOT Null
);

create table constit(
constituency varchar(30) primary key,
seats int not null,
con_type varchar (15) not null
);

create table politician( 
pol_id int Primary Key, 
first_name varchar(50) NOT NULL, 
second_name varchar(50) NOT NULL, 
title varchar(12) NOT NULL, 
email varchar(70) NOT NULL, 
role varchar (15) Not Null,
party_name varchar(6) Not null,
FOREIGN KEY (party_name) REFERENCES party(party_name),
constituency varchar(30) not null,
FOREIGN KEY (constituency) REFERENCES constit(constituency)
); 

create table reguser(
user_id int NOT NULL AUTO_INCREMENT primary key,
first_name varchar(50) NOT NULL, 
second_name varchar(50) NOT NULL, 
email varchar(70) NOT NULL, 
TD_constituency varchar(30) not null,
FOREIGN KEY (TD_constituency) REFERENCES constit(constituency),
Co_constituency varchar(30) not null,
FOREIGN KEY (Co_constituency) REFERENCES constit(constituency)
);



